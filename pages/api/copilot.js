// API Serverless Function สำหรับคุยโต้ตอบกับผู้บริหาร (Conversational Copilot - RAG)
// รองรับทั้ง Google Gemini API (แนะนำ - มีรุ่นฟรี), Anthropic Claude API หรือใช้ Mock สแตนด์บาย
